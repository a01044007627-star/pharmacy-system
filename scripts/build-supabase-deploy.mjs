import { promises as fs } from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")
const supabaseDir = path.join(root, "supabase")
const consolidatedDir = path.join(supabaseDir, "consolidated")
const migrationsDir = path.join(supabaseDir, "migrations")
const outputPath = path.join(supabaseDir, "deploy.sql")
const consolidatedFiles = ["000_core_tables.sql","001_helper_functions.sql","002_atomic_rpcs.sql","003_rls_policies.sql","004_triggers_views_constraints.sql","005_data_migrations.sql"]
const POST_CONSOLIDATION_CUTOFF = "20260619000000"
const migrationFiles = (await fs.readdir(migrationsDir))
  .filter((name) => name.endsWith(".sql") && name.slice(0,14) >= POST_CONSOLIDATION_CUTOFF)
  .sort((a,b) => a.localeCompare(b))
const files = [...consolidatedFiles.map((name) => path.join(consolidatedDir,name)),...migrationFiles.map((name) => path.join(migrationsDir,name))]
const sections = [
  "-- ============================================================================",
  "-- PHARMACY SYSTEM - FRESH DATABASE DEPLOYMENT",
  "-- Generated by scripts/build-supabase-deploy.mjs",
  "-- Existing databases should run supabase/final-repair.sql instead.",
  "-- ============================================================================","",
]
for (const file of files) {
  sections.push(`-- ===== ${path.basename(file)} =====`,(await fs.readFile(file,"utf8")).trim(),"")
}
await fs.writeFile(outputPath,`${sections.join("\n")}\n`,"utf8")
console.log(`Created ${path.relative(root,outputPath)} from ${files.length} ordered SQL files.`)
